kite_text = """A kite is traditionally a tethered heavier-than-air craft with wing surfaces that react against the air 
to create lift and drag. A kite consists of wings, tethers, and anchors. Kites often have a bridle to guide the face of 
the  kite at the correct angle so the wind can lift it. A kite's wing also may be so designed so a bridle is not needed; 
when kiting a sailplane for launch, the tether meets the wing at a single point. A kite may have fixed or moving 
anchors. Untraditionally in technical kiting, a kite consists of tether-set-coupled wing sets; even in technical kiting, 
though, a wing in the system is still often called the kite.

The lift that sustains the kite in flight is generated when air flows around the kite's surface, producing low pressure
above and high pressure below the wings. The interaction with the wind also generates horizontal drag along the
direction of the wind. The resultant force vector from the lift and drag force components is opposed by the tension of
one or more of the lines or tethers to which the kite is attached. The anchor point of the kite line may be static or
moving (such as the towing of a kite by a running person, boat, free-falling anchors as in paragliders and fugitive
parakites or vehicle).

The same principles of fluid flow apply in liquids and kites are also used under water.

A hybrid tethered craft comprising both a lighter-than-air balloon as well as a kite lifting surface is called a kytoon.

Kites have a long and varied history and many different types are flown individually and at festivals worldwide. Kites 
may be flown for recreation, art or other practical uses. Sport kites can be flown in aerial ballet, sometimes as part 
of a competition. Power kites are multi-line steerable kites designed to generate large forces which can be used to 
power activities such as kite surfing, kite landboarding, kite fishing, kite buggying and a new trend snow kiting. Even 
Man-lifting kites have been made.
"""

kite_history = """Kites were invented in China, where materials ideal for kite building were readily available: silk 
fabric for sail material; fine, high-tensile-strength silk for flying line; and resilient bamboo for a strong, 
lightweight framework.

The kite has been claimed as the invention of the 5th-century BC Chinese philosophers Mozi (also Mo Di) and Lu Ban (also 
Gongshu Ban). By 549 AD paper kites were certainly being flown, as it was recorded that in that year a paper kite was 
used as a message for a rescue mission. Ancient and medieval Chinese sources describe kites being used for measuring 
distances, testing the wind, lifting men, signaling, and communication for military operations. The earliest known 
Chinese kites were flat (not bowed) and often rectangular. Later, tailless kites incorporated a stabilizing bowline. 
Kites were decorated with mythological motifs and legendary figures; some were fitted with strings and whistles to make 
musical sounds while flying. From China, kites were introduced to Cambodia, Thailand, India, Japan, Korea and the 
western world.

After its introduction into India, the kite further evolved into the fighter kite, known as the patang in India, where 
thousands are flown every year on festivals such as Makar Sankranti.

Kites were known throughout Polynesia, as far as New Zealand, with the assumption being that the knowledge diffused from 
China along with the people. Anthropomorphic kites made from cloth and wood were used in religious ceremonies to send 
prayers to the gods. Polynesian kite traditions are used by anthropologists get an idea of early "primitive" Asian 
traditions that are believed to have at one time existed in Asia.
"""
